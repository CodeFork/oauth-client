namespace Promact.OAuth.Client.DomainModel
{
    /// <summary>
    /// Promact User and Role
    /// </summary>
    public class UserRole
    {
        /// <summary>
        /// User's Id
        /// </summary>
        public string UserId { get; set; }

        /// <summary>
        /// User's username
        /// </summary>
        public string UserName { get; set; }

        /// <summary>
        /// User's Name
        /// </summary>
        public string Name { get; set; }

        /// <summary>
        /// User's role
        /// </summary>
        public string Role { get; set; }
    }
}
